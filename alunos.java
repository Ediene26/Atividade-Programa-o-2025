public class alunos {
   private String nome;
   private  int matricula, notas;
   
   public String getNome() {
    return nome;
   }
   public void setNome(String nome) {
    this.nome = nome;
   }
   public int getMatricula() {
    return matricula;
   }
   public void setMatricula(int matricula) {
    this.matricula = matricula;
   }
   public int getNotas() {
    return notas;
   }
   public void setNotas(int notas) {
    this.notas = notas;
   }

}
